<?php

function calSuma( $valor1, $valor2){
    $resultado = $valor1 + $valor2;
    return $resultado;
}

function calResta( $valor1, $valor2){
    $resultado = $valor1 - $valor2;
    return $resultado;
}

function calMulti( $valor1, $valor2){
    $resultado = $valor1 * $valor2;
    return $resultado;
}

function calDivision( $valor1, $valor2){
    $resultado = $valor1 / $valor2;
    return $resultado;
}

function calMod( $valor1, $valor2){
    $resultado = $valor1 % $valor2;
    return $resultado;
}

function calPot( $valor1, $valor2){
    $resultado = $valor1 ** $valor2;
    return $resultado;
}

?>